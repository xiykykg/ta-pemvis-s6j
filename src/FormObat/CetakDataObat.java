    public void Cetak(){
        String html = "<H4><center>Laporan Data Obat Rumah Sakit Umum Taruna</center></h4>"+
                "<table width='98%' border='0' align='center' cellpadding='5' cellspacing='1'>"+
                    "<tr>"+
                        "<td bgcolor='#CCCCCC' align='center'><strong>Kode Obat</Strong></td>"+
                        "<td bgcolor='#CCCCCC' align='center'><strong>Nama Obat</Strong></td>"+
                        "<td bgcolor='#CCCCCC' align='center'><strong>Jenis Obat</Strong></td>"+
                        "<td bgcolor='#CCCCCC' align='center'><strong>Harga</Strong></td>"+
                    "<tr>";
        
        try{
            String sql = "SELECT * FROM obat";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                html = html + 
                    "<tr>" +
                        "<td bgcolor='#EEEEEE'>"+res.getString("kode_obat")+"</td>"+
                        "<td bgcolor='#F8F8F8'>"+res.getString("nama_obat")+"</td>"+
                        "<td bgcolor='#F8F8F8'>"+res.getString("jenis_obat")+"</td>"+
                        "<td bgcolor='#F8F8F8'>"+res.getString("harga")+"</td>"+
                    "</tr>";
            }
            
            EditorKit x = new HTMLEditorKit();
            tampilcetak.setEditorKit(x);
            tampilcetak.setText(html);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
