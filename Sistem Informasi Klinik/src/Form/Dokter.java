    protected void datatable(){ 
    Object[] Baris = {"Kode Dokter", "Nama Dokter", "Jenis Kelamin", "No Telepon", "Alamat"}; 
    tabmode = new DefaultTableModel (null, Baris); 
    tabeldokter.setModel(tabmode); 
    String cariitem=tcari.getText();
    try { 
        String sql = "select * from dokter where idd like '%"+cariitem+"%' or namad like '%"+cariitem+"%' order by idd asc";
        java.sql.Statement stat = conn.createStatement(); 
        ResultSet hasil = stat.executeQuery(sql); 
        while(hasil.next()) { 
            tabmode.addRow (new Object[]{ 
                    hasil.getString(1), 
                    hasil.getString(2), 
                    hasil.getString(3), 
                    hasil.getString(4), 
                    hasil.getString(5) 
                }); 
            }
            tabeldokter.setModel(tabmode); 
        } catch (Exception e) { 
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }
    }
